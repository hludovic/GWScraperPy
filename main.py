import sys
from scraper.GWScraper import GWScraper as scraper

def display_help():
    print("GWScraper 0.4.1")
    print("")
    print("Usage: ")
    print("")
    print("   -j   export a json file")
    print("   -c   export a csv file")

arguments_count = len(sys.argv)
if arguments_count == 2:
    first_argument = sys.argv[1]
    ds = scraper()

    if first_argument == "-j":
        ds.getJsonData()
    elif first_argument == "-c":
        ds.getCSVData()
    else:
        display_help()
else:
    display_help()